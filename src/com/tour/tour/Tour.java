package com.tour.tour;

public class Tour{
	
	private int tourId;
	private String tourName;
	private String tourStyle;
	private String tourCountry;
	private String tourCity;
	private String tourAge;
	private int tourMoney;
	private String tourSignStart;	//报名开始时间
	private String tourSignEnd;		//报名截至日期
	private String tourGo;
	private int tourNeedPerson;	//总共需要的人数
	private int tourSigned;		//已经报名的人数
	private int tourDeposit;	//需要支付的订金
	private String tourInfo;
	private int tourStatus;
	private String tourDay;		//游学总天数
	
	public int getTourId() {
		return tourId;
	}
	public void setTourId(int tourId) {
		this.tourId = tourId;
	}
	
	public String getTourName() {
		return tourName;
	}
	public void setTourName(String tourName) {
		this.tourName = tourName;
	}
	
	public String getTourStyle() {
		return tourStyle;
	}
	public void setTourStyle(String tourStyle) {
		this.tourStyle = tourStyle;
	}
	
	public String getTourCountry() {
		return tourCountry;
	}
	public void setTourCountry(String tourCountry) {
		this.tourCountry = tourCountry;
	}
	
	public String getTourCity() {
		return tourCity;
	}
	public void setTourCity(String tourCity) {
		this.tourCity = tourCity;
	}
	
	public String getTourAge() {
		return tourAge;
	}
	public void setTourAge(String tourAge) {
		this.tourAge = tourAge;
	}
	
	public int getTourMoney() {
		return tourMoney;
	}
	public void setTourMoney(int tourMoney) {
		this.tourMoney = tourMoney;
	}
	
	public String getTourSignStart() {
		return tourSignStart;
	}
	public void setTourSignStart(String tourSignStart) {
		this.tourSignStart = tourSignStart;
	}
	
	public int getTourNeedPerson() {
		return tourNeedPerson;
	}
	public void setTourNeedPerson(int tourNeedPerson) {
		this.tourNeedPerson = tourNeedPerson;
	}
	
	public String getTourSignEnd() {
		return tourSignEnd;
	}
	public void setTourSignEnd(String tourSignEnd) {
		this.tourSignEnd = tourSignEnd;
	}
	
	public String getTourGo() {
		return tourGo;
	}
	public void setTourGo(String tourGo) {
		this.tourGo = tourGo;
	}
	
	public int getTourSigned() {
		return tourSigned;
	}
	public void setTourSigned(int tourSigned) {
		this.tourSigned = tourSigned;
	}
	
	public int getTourDeposit() {
		return tourDeposit;
	}
	public void setTourDeposit(int tourDeposit) {
		this.tourDeposit = tourDeposit;
	}
	
	public String getTourInfo() {
		return tourInfo;
	}
	public void setTourInfo(String tourInfo) {
		this.tourInfo = tourInfo;
	}
	
	public int getTourStatus() {
		return tourStatus;
	}
	public void setTourStatus(int tourStatus) {
		this.tourStatus = tourStatus;
	}
	
	public String getTourDay() {
		return tourDay;
	}
	public void setTourDay(String tourDay) {
		this.tourDay = tourDay;
	}
	
}
